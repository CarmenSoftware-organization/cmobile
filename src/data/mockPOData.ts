// Helper function to get locations from POs
export const getLocationsFromPOs = (pos: PurchaseOrder[]) => {
  const locationMap = new Map<string, {
    name: string;
    itemCount: number;
    items: string[];
    departments: string[];
    poNumbers: string[];
  }>();

  pos.forEach(po => {
    po.items.forEach(item => {
      const location = item.itemDetail.storeLocation;
      if (!locationMap.has(location)) {
        locationMap.set(location, {
          name: location,
          itemCount: 0,
          items: [],
          departments: [],
          poNumbers: []
        });
      }

      const locationInfo = locationMap.get(location)!;
      locationInfo.itemCount += 1; // Count number of items, not quantities
      locationInfo.items.push(item.product);
      
      if (!locationInfo.departments.includes(item.itemDetail.department)) {
        locationInfo.departments.push(item.itemDetail.department);
      }
      
      if (!locationInfo.poNumbers.includes(po.number)) {
        locationInfo.poNumbers.push(po.number);
      }
    });
  });

  return Array.from(locationMap.values());
};

// Helper function to simulate PO scanning
export interface ScannedPOResult {
  found: boolean;
  po?: PurchaseOrder;
  hasBU: boolean;
  error?: string;
}

export const simulatePOScan = (code: string): ScannedPOResult => {
  const po = mockPOs.find(p => p.number === code || p.number.includes(code));
  
  if (!po) {
    return {
      found: false,
      hasBU: false,
      error: "PO not found"
    };
  }

  return {
    found: true,
    po,
    hasBU: !!po.businessUnit
  };
}; 